package after.facade;

public class PopcornPopper {
    public void on() {
        System.out.println("Popcorn popper - turning on for maximum deliciousness ahead");
    }

    public void pop() {
        System.out.println("Popcorn popper is a-popping!");
    }

    public void off() {
        System.out.println("Popcorn popper - off");
    }
}
